
int main() {
  int a = 5;
  float b = 3.14;
  char c = 'c';

  if (a > 0) {
    printf("The value of a is positive\n");
  } else {
    printf("The value of a is not positive\n");
  }

  if (b > 0) {
    printf("The value of b is positive\n");
  } else {
    printf("The value of b is not positive\n");
  }

  if (c > 0) {
    printf("The value of c is positive\n");
  } else {
    printf("The value of c is not positive\n");
  }

  return 0;
}